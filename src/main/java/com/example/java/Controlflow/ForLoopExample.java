public class ForLoopExample {
    public static void main(String[] args) {
        // Using a for loop to print numbers from 1 to 10
        for (int i = 1; i <= 10; i++) {
            // Print the current value of 'i'
            System.out.println("Number: " + i);
        }
    }
}

